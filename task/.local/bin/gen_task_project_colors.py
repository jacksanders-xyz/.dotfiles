PALETTES = {
    "night": PALETTE_NIGHT,
    "mid-day": PALETTE_MIDDAY,
    "morning": PALETTE_MORNING,
}

HEADER = [
    "# Auto-generated by gen_task_project_colors.py",
    "# Colors are randomized each run. Do not edit manually.",
    "",
]


def get_daypart() -> str:
    """Read the current theme from ~/.config/env/daypart.lua (e.g. return 'night')."""
    try:
        text = DAYPART_FILE.read_text().strip()
        m = re.search(r"return\s+['\"](\S+)['\"]", text)
        if m and m.group(1) in PALETTES:
            return m.group(1)
    except FileNotFoundError:
        pass
    return "mid-day"  # fallback


def run_task(args, timeout=15) -> str:
    # IMPORTANT: disable hooks to avoid recursion when called from a hook
    cmd = [
        "task",
        "rc.hooks=off",
        "rc.confirmation=off",
        "rc.verbose=nothing",
        *args,
    ]
    p = subprocess.run(cmd, capture_output=True, text=True, timeout=timeout)
    if p.returncode != 0:
        msg = (p.stderr or p.stdout).strip()
        raise RuntimeError(msg or f"task failed: {' '.join(cmd)}")
    return p.stdout


def get_projects():
    # Use pending projects only (fast). Change to ["_projects"] if you want all.
    out = run_task(["status:pending", "_projects"])
    return sorted({line.strip() for line in out.splitlines() if line.strip()})


def color_stream(palette, rng: random.Random):
    """
    Infinite generator of colors where:
    - colors are shuffled
    - no repeats until the palette is exhausted
    - then reshuffle and continue
    """
    while True:
        bag = palette[:]
        rng.shuffle(bag)
        for c in bag:
            yield c


def main():
    part = get_daypart()
    palette = PALETTES[part]
    projects = get_projects()

    # New random assignment each run.
    # If you want it to be repeatable for a given day/session, set a seed.
    rng = random.Random()  # system entropy
    colors = color_stream(palette, rng)

    OUTFILE.parent.mkdir(parents=True, exist_ok=True)

    lines = HEADER[:]
    for p in projects:
        c = next(colors)
        lines.append(f"color.project.{p}={c}")

    # Overwrite file each run (this is the "jumble" behavior)
    OUTFILE.write_text("\n".join(lines) + "\n")

    print(f"Wrote {OUTFILE} with {len(projects)} projects ({part} palette).")


if __name__ == "__main__":
    main()

